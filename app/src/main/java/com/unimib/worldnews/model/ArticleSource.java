package com.unimib.worldnews.model;

public class ArticleSource {
    private String id;
    private String name;

    public ArticleSource() {}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
